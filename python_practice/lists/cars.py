# sorting using sort method
cars = ['bentley', 'posrche', 'mustang', 'audi', 'toyota', 'gts']
len(cars)
print(cars)
cars.sort()
print(cars)

# sorting in a reverse alphabetical manner
cars = ['cayenne', 'auris', 'cullinun', 'bentyga', 'mitsubishi', 'auris']
len(cars)
print(cars)
cars.sort(reverse = True)
print(cars)

# sorting temporarily using sorted method
cars = ['cadillac', 'lexus', 'toureg', 'discovery', 'lamborghini', 'prius']
print('this is the original list of the cars')
