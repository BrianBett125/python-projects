## seing the world
places = ['canada', 'america', 'tokyo', 'guatamela', 'sudan']
print(places)

## invoking the sorted() method
print(sorted(places))
print("list in the original order")
print(places)

## Use sorted() to print your list in reverse-alphabetical order
places.reverse()
print(places)
places.reverse()
print(places)
places.sort()
print(places)
places.sort(reverse = True)
print(places)

