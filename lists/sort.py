cars = ['bentley', 'subaru', 'mercedes']
print(cars)
print("\nsorting  a list permanently")
cars.sort()
print(cars)

print("\nsorting a list alphabetically in reverse order")
cars.sort(reverse = True)
print(cars)

print("\nsorting a list temporarily using sorted()")
print(sorted(cars))

print("\nprinting a list in reverse order")
cars.reverse()
print(cars)

print("\nfinding the length of a list")
len(cars)


